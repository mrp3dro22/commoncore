#include "libft.h"

void	ft_putnbr(int fd, int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar(fd, '-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(fd, nb / 10);
		ft_putchar(fd, nb % 10 + '0');
	}
	else
		ft_putchar(fd, nb + '0');
}